package matematica.avancado.funcaoquadratica;

// Variantes da pergunta sobre o vértice de uma parábola com concavidade para baixo
// f(x) = -a·x² + b·x + c (máximo no vértice):
//   VALOR:    pede a ordenada do vértice (altura/área/lucro máximo);
//   ABSCISSA: pede a abscissa do vértice (instante/largura/quantidade ótima).
public enum TipoFuncaoQuadratica
{
	VALOR, ABSCISSA
}
