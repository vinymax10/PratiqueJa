package Matematica;


public class DefinicaoCores
{
	public static String iris()
	{
		return "\\definecolor{iris}{rgb}{0.39, 0.44, 1}";
	}
	
	public static String babypink()
	{
		return "\\definecolor{babypink}{rgb}{1, 0.42, 0.52}";
	}
	
	public static String irisBabypink()
	{
		return "\\definecolor{iris}{rgb}{0.39, 0.44, 1}"
		+ " \\definecolor{babypink}{rgb}{1, 0.42, 0.52}";
	}
}